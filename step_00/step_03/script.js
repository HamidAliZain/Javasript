// Arithmetic Operators:
// Addition (+)
var sum = 5 + 3; // 8

// Subtraction (-)
var sub = 10 - 5; // 5

// Multiplication (*)
var product = 3 * 7; // 21

//  Division (/)
var quotient = 15 / 3; // 5

//  Remainder (%)
var remainder = 10 % 3; // 1

// Increment (++) and Decrement (--)
var count = 5;
count++; // Increment by 1
count--; // Decrement by 1

// Comparison Operators:
// Equal (==)
var isEqual = 5 == "5"; // true (loose equality)

//  Strict Equal (===)
var isStrictEqual = 5 === "5"; // false (strict equality)

//  Not Equal (!=)
var isNotEqual = 10 != 8; // true

//  Strict Not Equal (!==)
var isStrictNotEqual = 10 !== "10"; // true

//  Greater Than (>)
var greater = 10 > 5; // true
//  Less Than (>)
var less = 8 < 3; // false
//  Greater Than or Equal (>=);
var greaterOrEqual = 7 >= 7; // true
//   Less Than or Equal (<=)
var lessOrEqual = 5 <= 2; // false

//   Logical Operators:
//   AND (&&)
var result = true && false; // false

//   OR (||)
var result2 = true || false; // true

//   Assignment Operators:
//   Addition Assignment (+=)
var y = 5;
y += 3; // y is now 8

// Subtraction Assignment (-=)
var z = 10;
z -= 4; // z is now 6

// Conditional (Ternary) Operator (? :):
var age = 20;
var status = age >= 18 ? "Adult" : "Minor";
